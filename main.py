import turtle as t
import random as r

color_list = [(54, 108, 149), (225, 201, 108), (134, 85, 58), (229, 235, 234), (224, 141, 62), (197, 144, 171),
              (143, 180, 206), (137, 82, 106), (210, 90, 68), (232, 226, 194), (188, 78, 122), (69, 101, 86),
              (132, 183, 132),
              (65, 156, 86), (137, 132, 74), (48, 155, 195), (183, 191, 202), (232, 221, 225), (58, 47, 41),
              (47, 59, 96),
              (38, 44, 64), (106, 46, 54), (41, 55, 48), (12, 104, 95), (118, 125, 145), (182, 194, 199),
              (215, 176, 187),
              (223, 178, 168), (54, 45, 52), (179, 199, 184), (133, 41, 39), (76, 63, 49), (38, 79, 82)]

t.colormode(255)
t.pensize(20)

t.penup()
t.setx(-250)
t.sety(-250)

def create_paint():
    for j in range(10):
        for i in range(10):
            t.color(r.choice(color_list))
            t.pendown()
            t.forward(1)
            t.penup()
            t.forward(50)
        t.left(90)
        t.forward(50)
        t.right(90)
        t.setx(-250)


create_paint()

t.exitonclick()
